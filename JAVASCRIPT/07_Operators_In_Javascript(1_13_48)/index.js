// 1.1. Arithmetic Operators:

var a=100;
var b=50;

console.log("The value of a+b is",a+b);
console.log("The value of a-b is",a-b);
console.log("The value of a*b is",a*b);
console.log("The value of a/b is",a/b);


// 2. Assign,ent Operators:
var c=b;
console.log(c);

// 3. Comparison operators.

var x=9;
var y=4;
console.log(x==y);
console.log(x>=y);
console.log(x<=y);
console.log(x<y);
console.log(x>y);

// 4. Logical Operators:

// a) Logical AND(&&)
// && - logical AND , &-bitwise and
console.log(true && true);
console.log(false && true);
console.log(false && false);

// b) Logical OR(||)
console.log(true || true);
console.log(false || true);
console.log(false || false);


// c) Logical NOT
console.log(!false);
console.log(!true);


